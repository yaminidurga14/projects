public class commonElements {
   public static void main(String[] args) {
    int[] arr1={14,3,9,5}; 
    int[] arr2={14,3}; 
    for(int i=0;i<arr1.length;i++){ 
        for(int j=0;j<arr2.length;j++){ 
          if(arr1[i]==(arr2[j])){ 
            System.out.println(arr1[i]); 
          } 
        } 
      } 
   } 
}
